     private void Show_Measures(){
      try{
      String ID = null;
      String nameBG = null;
      String shortNameBG = null;
      String nameEN = null;
      String shortNameEN = null;
      String nameDE = null;
      String shortNameDE = null;
      DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
      model.setRowCount(0);
      Connection con = getConnection();
      PreparedStatement ps = con.prepareStatement("select\n" +
                                                "  M.ID, M.NAME_DE, M.SHORT_NAME_DE, M.NAME_EN, M.SHORT_NAME_EN, M.NAME_BG, M.SHORT_NAME_BG\n" +
                                                "from\n" +
                                                "  N_MEASURES M");
       ResultSet rs=ps.executeQuery();
       while(rs.next()){
         ID = rs.getString("ID");
         nameBG = rs.getString("NAME_BG");
         shortNameBG = rs.getString("SHORT_NAME_BG");
         nameEN = rs.getString("NAME_EN");
         shortNameEN = rs.getString("SHORT_NAME_EN");
         nameDE = rs.getString("NAME_DE");
         shortNameDE = rs.getString("SHORT_NAME_DE");
         model.addRow(new Object[]{ID, nameBG, shortNameBG, nameEN, shortNameEN, nameDE, shortNameDE});
       }
      }catch(SQLException e){
        Logger.getLogger(ConsultationDelivery.class.getName()).log(Level.SEVERE, null, e);
      }
    }
