    private void jtbnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jtbnAddActionPerformed
     if(jxtFullNameBG.getText().isEmpty() || jtxtFullNameEN.getText().isEmpty() || jtxtFullNameDE.getText().isEmpty() ||
         jtxtShortNameBG.getText().isEmpty() || jtxtShortNameEN.getText().isEmpty() || jtxtShortNameDE.getText().isEmpty()){
             JOptionPane.showMessageDialog(this, "Моля попълнете всички полета!");
        }else{
          try{                 
          Connection con = getConnection();
          PreparedStatement ps = con.prepareStatement("insert into N_MEASURES (NAME_DE, SHORT_NAME_DE, NAME_EN, SHORT_NAME_EN, NAME_BG, SHORT_NAME_BG)\n" +
                                                        "values (?, ?, ?, ?, ?, ?)");
           ps.setString(1, jtxtFullNameDE.getText());
           ps.setString(2, jtxtShortNameDE.getText());
           ps.setString(3, jtxtFullNameEN.getText());
           ps.setString(4, jtxtShortNameEN.getText());
           ps.setString(5, jxtFullNameBG.getText());
           ps.setString(6, jtxtShortNameBG.getText());
           
           ps.executeUpdate();
           JOptionPane.showMessageDialog(null,"Записано в базата данни!");
           jtxtID.setText("");
           jxtFullNameBG.setText("");
           jtxtFullNameEN.setText("");
           jtxtFullNameDE.setText("");
           jtxtShortNameDE.setText("");
           jtxtShortNameBG.setText("");
           jtxtShortNameEN.setText("");

           Show_Measures();
           
          }catch(HeadlessException | SQLException ex){
             JOptionPane.showMessageDialog(null,ex.getMessage());
            }
         }
    }//GEN-LAST:event_jtbnAddActionPerformed

    private void jtbnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jtbnUpdateActionPerformed
        if(jTable1.getSelectedRow() == -1 || jtxtID.getText().isEmpty()){
             JOptionPane.showMessageDialog(this, "Моля изберете мерната единица, която искате да редактирате!");
        }else{
          try{                 
          Connection con = getConnection();
          PreparedStatement ps = con.prepareStatement("update N_MEASURES\n" +
                                                        "set NAME_DE = ?,\n" +
                                                        "    SHORT_NAME_DE = ?,\n" +
                                                        "    NAME_EN = ?,\n" +
                                                        "    SHORT_NAME_EN = ?,\n" +
                                                        "    NAME_BG = ?,\n" +
                                                        "    SHORT_NAME_BG = ?\n" +
                                                        "where (ID = ?)");
           ps.setString(1, jtxtFullNameDE.getText());
           ps.setString(2, jtxtShortNameDE.getText());
           ps.setString(3, jtxtFullNameEN.getText());
           ps.setString(4, jtxtShortNameEN.getText());
           ps.setString(5, jxtFullNameBG.getText());
           ps.setString(6, jtxtShortNameBG.getText());
           ps.setString(7, jtxtID.getText());
           ps.executeUpdate();
           JOptionPane.showMessageDialog(null,"Успешно направихте промени!");
           
           jtxtID.setText("");
           jxtFullNameBG.setText("");
           jtxtFullNameEN.setText("");
           jtxtFullNameDE.setText("");
           jtxtShortNameDE.setText("");
           jtxtShortNameBG.setText("");
           jtxtShortNameEN.setText("");

           Show_Measures();
           jtbnAdd.setEnabled(true);
          }catch(HeadlessException | SQLException ex){
             JOptionPane.showMessageDialog(null,ex.getMessage());
            }
        }
    }//GEN-LAST:event_jtbnUpdateActionPerformed

