    public void createServer() throws IOException{
     
      HttpServer server = HttpServer.create(new InetSocketAddress(8500), 0);
      server.createContext("/api/v1/articles", new ArticleHandler());
      server.createContext("/api/v1/categories", new CategoryHandler());
      server.createContext("/api/v1/clients", new ClientHandler());
      server.createContext("/api/v1/masterDeals", new MasterDealsHandler());
      server.createContext("/api/v1/detailDeals", new DetailDealsHandler());
      server.start();
        
    }
    

