    private void jbtnSOAPActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnSOAPActionPerformed
        try {
            String fullVatNumber = "DE308177468";
            //String countryCode = "DE";
            //String vatNumber = "308177468";
            String countryCode = fullVatNumber.substring(0, 2);
            String vatNumber = fullVatNumber.substring(2, fullVatNumber.length());

            // Create SOAP Connection
            SOAPConnectionFactory soapConnectionFactory = SOAPConnectionFactory.newInstance();
            SOAPConnection soapConnection = soapConnectionFactory.createConnection();

            // Send SOAP Message to SOAP Server
            String url = "http://ec.europa.eu/taxation_customs/vies/services/checkVatService";
            SOAPMessage soapResponse = soapConnection.call(createSOAPRequest(countryCode, vatNumber), url);

            // Process the SOAP Response
            String isValid = printSOAPResponse(soapResponse);
            String validBG = "";
            if("true".equals(isValid)) 
                validBG = "Валиден";
            else
                validBG = "Невалиден";
            JOptionPane.showMessageDialog(null, validBG); 
            soapConnection.close();
        } catch (Exception e) {
            System.err.println("Грешка при изпращането на заявка до сървъра!");
            JOptionPane.showMessageDialog(null, "Грешка при изпращането на заявка до сървъра!"); 
            e.printStackTrace();
        }
    }//GEN-LAST:event_jbtnSOAPActionPerformed

